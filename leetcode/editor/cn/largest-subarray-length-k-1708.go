func largestSubarray(nums []int, k int) []int {
	t, res := nums[0], make([]int, 0)
	for i := 1; i < len(nums)-k; i++ {
		if t < nums[i] {
			t = nums[i]
			res = nums[i : i+k]
		}
	}

	return res
}

