/**
 * Definition for singly-linked list.
 * type ListNode struct {
 *     Val int
 *     Next *ListNode
 * }
 */
func removeNthFromEnd(head *ListNode, n int) *ListNode {
	// 1 -> 2 -> 3 -> 4 -> 5, n= 2, remove 4
	// l         r
	//           l    ()    r

	// l, r are reference
	l, r := head, head
	for step := 0; step < n; step++ {
		r = r.Next
	}
	if r == nil {
		return l.Next
	}
	for r.Next != nil {
		l, r = l.Next, r.Next
	}
	t := l.Next
	l.Next = t.Next
	t.Next = nil

	return head
}

